""""
str - string
"""

print("Aspas duplas")
print('Aspas simples')

print("Essa é uma 'String'(str).")
print('Essa é uma "String"(str).')

# caracteres de escape \
# não fica esteticamente bom
print("Essa é uma \"String\"(str).")
print('Essa é uma \'String\'(str).')

# r antes das Aspas diz para interpretador que nada deve ser executado
print(r"Esse é meu \n (str).")
